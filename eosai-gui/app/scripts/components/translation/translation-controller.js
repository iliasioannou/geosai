var determineCurrentLanguage= function () {

 var userLang = navigator.language || navigator.browserLanguage;
 if (userLang.indexOf("it")>-1){
   userLang="it";
 }else if (userLang.indexOf("gr")>-1){
   userLang="gr";
 }else {
   userLang="en";
 }
 return userLang;
};


